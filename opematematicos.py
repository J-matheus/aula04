print (f"Do calculo {n1}-{n2} o valor da subtração é: {sub} ")
print (f"Do calculo {n1}*{n2} o valor da multiplicação é: {multip} ")
print (f"Do calculo {n1}/{n2} o valor da divisão é: {div} ")